def _make_market_ctx(
    trend="bullish",
    strength=0.6,
    vol=0.5,
    vol_ratio=1.8,
    regime="trending",
    sentiment="neutral",
):
    return MarketContext(
        trend_direction=trend,
        trend_strength=strength,
        volatility_percentile=vol,
        volume_ratio=vol_ratio,
        market_regime=regime,
        sentiment=sentiment,
    )


def _make_confidence():
    return ConfidenceBreakdown(
        indicator_agreement=0.85,
        volume_confirmation=0.75,
        trend_alignment=0.70,
        volatility_context=0.60,
        overall_confidence=0.76,
        quality_grade="B",
    )


class TestGenerateExplanation:
    def test_buy_signal_with_factors(self):
        factors = _make_factors()
        ctx = _make_market_ctx()
        explanation = generate_explanation("BUY", factors, ctx, None, [])

        assert "BUY signal triggered" in explanation
        assert "RSI(14)" in explanation
        assert "MACD" in explanation

    def test_sell_signal(self):
        factors = [
            ContributingFactor(
                indicator_name="RSI(14)",
                value=72.0,
                threshold=70.0,
                condition="crossed_above",
                weight=0.5,
                description="RSI(14) crossed above 70 (overbought)",
            ),
        ]
        ctx = _make_market_ctx(trend="bearish", sentiment="bearish")
        explanation = generate_explanation("SELL", factors, ctx, None, [])

        assert "SELL signal triggered" in explanation
        assert "RSI(14)" in explanation

    def test_with_historical_context(self):
        factors = _make_factors()
        ctx = _make_market_ctx()
        hist = generate_historical_context(
            win_rate=0.67, signal_count=45, avg_return=2.1, lookback_days=90
        )
        explanation = generate_explanation("BUY", factors, ctx, hist, [])

        assert "67%" in explanation
        assert "90 days" in explanation

    def test_with_risk_factors(self):
        factors = _make_factors()
        ctx = _make_market_ctx(sentiment="bearish")
        risks = [
            RiskFactor(
                category="market",
                severity="medium",
                description="Overall market sentiment is bearish",
                impact_estimate=-0.12,
            )
        ]
        explanation = generate_explanation("BUY", factors, ctx, None, risks)

        assert "bearish" in explanation
        assert "12%" in explanation

    def test_empty_factors(self):
        ctx = _make_market_ctx()
        explanation = generate_explanation("BUY", [], ctx, None, [])
        assert "BUY signal generated by strategy rules" in explanation


class TestDetectRiskFactors:
    def test_bearish_sentiment(self):
        ctx = _make_market_ctx(sentiment="bearish")
        risks = detect_risk_factors(ctx, "validated")
        categories = [r.category for r in risks]
        assert "market" in categories

