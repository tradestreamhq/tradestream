"""
Reasoning generator — produces human-readable explanations for trading signals.

Captures which indicators triggered, their values and thresholds, market context,
historical patterns, and risk factors. Outputs a natural language explanation.
"""

import logging
import math
import statistics
from typing import Dict, List, Optional, Tuple

from services.prediction_reasoning.models import (
    ConfidenceBreakdown,
    ContributingFactor,
    HistoricalPattern,
    MarketContext,
    RiskFactor,
    SignalReasoning,
)

logger = logging.getLogger(__name__)

# Indicator condition templates
CONDITION_TEMPLATES = {
    "crossed_above": "{name} crossed above {threshold:.1f} (value: {value:.2f})",
    "crossed_below": "{name} crossed below {threshold:.1f} (value: {value:.2f})",
    "exceeded": "{name} exceeded {threshold:.1f} at {value:.2f}",
    "below": "{name} fell below {threshold:.1f} at {value:.2f}",
    "positive": "{name} turned positive ({value:.4f})",
    "negative": "{name} turned negative ({value:.4f})",
    "divergence": "{name} shows divergence (value: {value:.2f})",
}


def _format_factor_description(factor: ContributingFactor) -> str:
    """Generate human-readable description for a contributing factor."""
    template = CONDITION_TEMPLATES.get(factor.condition)
    if template:
        return template.format(
            name=factor.indicator_name,
            value=factor.value,
            threshold=factor.threshold,
        )
    return (
        f"{factor.indicator_name}: {factor.value:.2f} (condition: {factor.condition})"
    )


def _describe_market_context(ctx: MarketContext) -> str:
    """Generate human-readable market context description."""
    parts = []

    trend_desc = {
        "bullish": "bullish",
        "bearish": "bearish",
        "neutral": "neutral/sideways",
    }
    direction = trend_desc.get(ctx.trend_direction, ctx.trend_direction)

    strength_label = "weak"
    if ctx.trend_strength >= 0.7:
        strength_label = "strong"
    elif ctx.trend_strength >= 0.4:
        strength_label = "moderate"

    parts.append(f"Market trend is {strength_label} {direction}")

    if ctx.volatility_percentile >= 0.8:
        parts.append("volatility is elevated")
    elif ctx.volatility_percentile <= 0.2:
        parts.append("volatility is unusually low")

    if ctx.volume_ratio >= 1.5:
        parts.append(f"volume is {ctx.volume_ratio:.1f}x the 20-day average")
    elif ctx.volume_ratio <= 0.5:
        parts.append("volume is below average")

    if ctx.market_regime != "trending":
        parts.append(f"market regime: {ctx.market_regime}")

    return ". ".join(parts) + "."


def _describe_risk_factors(risks: List[RiskFactor]) -> str:
    """Generate human-readable risk description."""
    if not risks:
        return ""

    lines = []
    for rf in risks:
        impact = ""
        if rf.impact_estimate != 0:
            pct = abs(rf.impact_estimate * 100)
            direction = "reduced" if rf.impact_estimate < 0 else "improved"
            impact = f", which has historically {direction} win rate by {pct:.0f}%"
        lines.append(f"Note: {rf.description}{impact}")
    return " ".join(lines)


def generate_explanation(
    signal_type: str,
    factors: List[ContributingFactor],
    market_ctx: MarketContext,
    historical: Optional[HistoricalPattern],
    risk_factors: List[RiskFactor],
) -> str:
    """Generate a complete natural language explanation for a signal.

    Args:
        signal_type: BUY or SELL
        factors: List of contributing indicator factors
        market_ctx: Market context snapshot
        historical: Historical pattern match (optional)
        risk_factors: List of risk factors

    Returns:
        Human-readable explanation text.
    """
    parts = []

    # Primary signal reason
    if factors:
        sorted_factors = sorted(factors, key=lambda f: f.weight, reverse=True)
        primary = sorted_factors[0]
        secondary = sorted_factors[1:3]

        reason = f"{signal_type} signal triggered because {primary.description}"
        if secondary:
            secondary_descs = [f.description for f in secondary]
            reason += " while " + " and ".join(secondary_descs)
        parts.append(reason)
    else:
        parts.append(f"{signal_type} signal generated by strategy rules")

    # Market context
    ctx_desc = _describe_market_context(market_ctx)
    parts.append(ctx_desc)

    # Historical context
    if historical and historical.similar_signals_count > 0:
        parts.append(historical.description)

    # Risk factors
    risk_desc = _describe_risk_factors(risk_factors)
    if risk_desc:
        parts.append(risk_desc)

    return " ".join(parts)


def generate_historical_context(
    win_rate: float,
    signal_count: int,
    avg_return: float,
    lookback_days: int,
) -> HistoricalPattern:
    """Generate historical pattern context from past signal performance.

    Args:
        win_rate: Win rate as a decimal (e.g. 0.67 = 67%)
        signal_count: Number of similar signals in the lookback period
        avg_return: Average return of similar signals
        lookback_days: Number of days in the lookback period
    """
    pct = win_rate * 100
    description = (
        f"This pattern has resulted in profitable trades {pct:.0f}% of the time "
        f"over the past {lookback_days} days ({signal_count} similar signals, "
        f"avg return: {avg_return:+.2f}%)"
    )
    return HistoricalPattern(
        similar_signals_count=signal_count,
        win_rate=win_rate,
        avg_return=avg_return,
        lookback_days=lookback_days,
        description=description,
    )


def detect_risk_factors(
    market_ctx: MarketContext,
    validation_level: str,
    strategy_age_months: Optional[int] = None,
    recent_drawdown: Optional[float] = None,
) -> List[RiskFactor]:
    """Detect risk factors based on market context and strategy metadata.

    Args:
        market_ctx: Current market context
        validation_level: Strategy validation level
        strategy_age_months: How long the strategy has existed
        recent_drawdown: Recent max drawdown as decimal (e.g. -0.15)
    """
    risks = []

    # Bearish sentiment risk
    if market_ctx.sentiment == "bearish":
        risks.append(
            RiskFactor(
                category="market",
                severity="medium",
                description="Overall market sentiment is bearish",
                impact_estimate=-0.12,
            )
        )

    # High volatility risk
    if market_ctx.volatility_percentile >= 0.8:
        risks.append(
            RiskFactor(
                category="volatility",
                severity="high",
                description="Asset is experiencing elevated volatility",
                impact_estimate=-0.08,
            )
        )

    # Low volume risk
    if market_ctx.volume_ratio < 0.5:
        risks.append(
            RiskFactor(
                category="market",
                severity="medium",
                description="Trading volume is below average, signal may be less reliable",
                impact_estimate=-0.05,
            )
        )

    # Validation level risk
    if validation_level == "candidate":
        risks.append(
            RiskFactor(
                category="validation",
                severity="high",
                description="Strategy is backtest-only and has not been forward-tested",
                impact_estimate=-0.20,
            )
        )

    # New strategy risk
    if strategy_age_months is not None and strategy_age_months < 3:
        risks.append(
            RiskFactor(
                category="strategy",
                severity="medium",
                description=f"Strategy is relatively new ({strategy_age_months} months old)",
                impact_estimate=-0.10,
            )
        )

    # Recent drawdown risk
    if recent_drawdown is not None and recent_drawdown < -0.15:
        pct = abs(recent_drawdown * 100)
        risks.append(
            RiskFactor(
                category="strategy",
                severity="high",
                description=f"Strategy experienced {pct:.1f}% drawdown recently",
                impact_estimate=-0.15,
            )
        )

    return risks


def calculate_expected_return_with_ci(
    strategy_returns: List[float],
    confidence_level: float = 0.95,
) -> Tuple[float, float, float]:
    """Calculate expected return with confidence interval using t-distribution.

    Args:
        strategy_returns: List of historical returns (as percentages)
        confidence_level: Confidence level for interval (default 0.95)

    Returns:
        Tuple of (mean, ci_lower, ci_upper)
    """
    if len(strategy_returns) < 2:
        mean = strategy_returns[0] if strategy_returns else 0.0
        return (mean, mean, mean)

    mean = statistics.mean(strategy_returns)
    std = statistics.stdev(strategy_returns)
    n = len(strategy_returns)

    # Use normal approximation for large samples, conservative estimate for small
    if n >= 30:
        z = 1.96 if confidence_level == 0.95 else 2.576
        margin = z * (std / math.sqrt(n))
    else:
        # Approximate t-value for small samples (conservative)
        # t_{0.975, n-1} approximation
        df = n - 1
        t_value = 2.0 + 4.0 / df  # rough upper bound
        margin = t_value * (std / math.sqrt(n))

    return (mean, mean - margin, mean + margin)


def build_reasoning(
    signal_id: str,
    strategy_name: str,
    symbol: str,
    signal_type: str,
    factors: List[ContributingFactor],
    indicator_values: Dict[str, float],
    market_ctx: MarketContext,
    confidence: ConfidenceBreakdown,
    validation_level: str = "candidate",
    historical_win_rate: Optional[float] = None,
    historical_signal_count: int = 0,
    historical_avg_return: float = 0.0,
    historical_lookback_days: int = 90,
    strategy_age_months: Optional[int] = None,
    recent_drawdown: Optional[float] = None,
    strategy_returns: Optional[List[float]] = None,
    predicted_timeframe_hours: Optional[int] = None,
) -> SignalReasoning:
    """Build a complete SignalReasoning from signal components.

    This is the main entry point for generating reasoning at signal time.
    """
    # Auto-generate descriptions for factors that don't have them
    for f in factors:
        if not f.description:
            f.description = _format_factor_description(f)

    # Detect risk factors
    risk_factors = detect_risk_factors(
        market_ctx, validation_level, strategy_age_months, recent_drawdown
    )

    # Generate historical context
    historical = None
    if historical_win_rate is not None and historical_signal_count > 0:
        historical = generate_historical_context(
            historical_win_rate,
            historical_signal_count,
            historical_avg_return,
            historical_lookback_days,
        )

    # Generate explanation
    explanation = generate_explanation(
        signal_type, factors, market_ctx, historical, risk_factors
    )

    # Calculate expected return with CI
    expected_return = None
    ci_lower = None
    ci_upper = None
    if strategy_returns and len(strategy_returns) >= 5:
        expected_return, ci_lower, ci_upper = calculate_expected_return_with_ci(
            strategy_returns
        )

    return SignalReasoning(
        signal_id=signal_id,
        strategy_name=strategy_name,
        symbol=symbol,
        signal_type=signal_type,
        contributing_factors=factors,
        indicator_values=indicator_values,
        market_context=market_ctx,
        confidence_breakdown=confidence,
        explanation_text=explanation,
        historical_pattern=historical,
        risk_factors=risk_factors,
        validation_level=validation_level,
        expected_return=expected_return,
        expected_return_ci_lower=ci_lower,
        expected_return_ci_upper=ci_upper,
        predicted_timeframe_hours=predicted_timeframe_hours,
    )
